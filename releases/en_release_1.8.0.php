		<ul>
		<li>RaspberryPi SPS</li>
		<li>RaspberryPi and BeagleBone black based on sysfs</li>
		<li>Lego Mindstorms EV3 (touch sensor, LEDs)</li>
		</ul>
	</li>
	<li>Improve UI:
		<ul>
		<li>Move functionality of algorithm tab and internal variable tab to property sheets</li>
		<li>Improvements for ST editor</li>
