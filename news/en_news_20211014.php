<p>Have you been waiting for 4diac 2.0.0? It's finally here!</p>

<p>We've been working on improving your experience with Eclipse 4diac. For 4diac IDE, the following major changes were introduced:</p>
<ul>
	<li>Improved usabilty and editing efficiency of all editors</li>
	<li>Introduced monitoring for Structured Datatypes</li>
	<li>Introduced editing history during undo/redo to quickly find the last editing position</li>
	<li>Improved layout generation results</li>
	<li>Improved/introduced input validation, show error messages</li>
	<li>Introduced error markers to handle broken projects more gracefully</li>
	<li>Of course, we have also implemented numerous bug fixes.</li>
</ul>

<p>For 4diac FORTE, we have mostly provided bug fixes and stabilization patches:</p>
<ul>
	<li>Gracefully handling full external event queues</li>
	<li>Fixed issues for compiling on Mac OS</li>
	<li>Implicit upcasts are allowed for constant inputs at FBs (e.g., WORD#AFFE on a DWORD input)</li>
</ul>


<p>For details, please consider the release page of our <a href="newandnoteworthy/2.0">New and Noteworthy</a>.</p>

<p>We sincerely hope that you enjoy our graduation release. We are looking foward to sharing more documentation on the new features in the upcoming weeks.</p>

