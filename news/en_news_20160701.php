<p> As in the last years we gathered excellent speakers and provide you with a great set of presentations. 
They cover smart grid applications, robot programming, OPC UA integration, formal specification methods, 
and how to use 4diac in teaching and training.</p> 

<p>In addition to the presentations we have allocated time for working hands on with 4diac and discussing with users and 4diac 
developers about the use 4diac and future development of 4diac.</p>

