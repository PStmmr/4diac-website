<p>It is a great pleasure for us to announce that the 4DIAC Users' Workshop will be held again in conjunction with the 
<a href="http://www.etfa2016.org/">IEEE ETFA conference</a>, Berlin. </p>

<p>The main goal of the 4DIAC Users' Workshop is to allow you to present your work and projects related to 4DIAC, 
exchange ideas, and discuss the future enhancements and features of 4DIAC with the development team. 
In addition to presentations this year&#39;s workshop will also provide the possibility for people that are not 
able to join us in person to present their use of 4DIAC in form of a poster.</p>

<p>Furthermore this year we'll have also a hardware exhibition with control devices able to execute IEC 61499 
applications modeled with 4DIAC. 
The 4DIAC team has already some devices they plan to show but we are also looking forward that you bring your own devices!<p>

<p>You can find more information about the 6th 4DIAC Users' Workshop and also information about the previos workshops at the 
<a href="en_uws.php">4DIAC Users' workshop page</a>.</p>

<p>The deadline for the submission of workshop presentation, poster, and hardware exhibition proposals, is April 30th, 2016. 

<p>If you have any question, do not hesitate to contact us.</p>

