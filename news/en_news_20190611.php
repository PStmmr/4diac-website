<p>I just finished the first test phase of the dynamic type loader. 
To summarize, simple Basic FBs can already deployed without any troubles, e.g., calculations, FOR loops and Conditions (i.e., IF, CASE).
In addition, all types of ECCs i found and internal variables also work. Datatypes seem to be working too, except Time Types. 
There is an open bug for this already. For functions, there is an open bug too.</p>
 
<p>I newly discovered, that some of the simple Structured Text functionality is still not supported, like WHILE/REPEAT loops or standard functions.</p> 

